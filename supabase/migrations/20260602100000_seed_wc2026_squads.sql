-- 1) Upsert : crée/actualise chaque joueur de la liste définitive, le (ré)active.
insert into ref.players (team_id, name, display_name, position, shirt_number, club, active)
select t.id, s.name, s.display_name, s.position, s.shirt_number, s.club, true
from _wc_squads s
join ref.teams t on t.fifa_code = s.fifa_code
on conflict (team_id, lower(name)) do update set
  display_name = excluded.display_name,
  position     = excluded.position,
  shirt_number = excluded.shirt_number,
  club         = excluded.club,
  active       = true,
  updated_at   = now();

-- 2) Désactive les joueurs des équipes importées absents de la liste définitive (sans suppression).
update ref.players p
set active = false, updated_at = now()
from ref.teams t
where p.team_id = t.id
  and t.fifa_code in (select distinct fifa_code from _wc_squads)
  and p.active
  and not exists (
    select 1 from _wc_squads s
    where s.fifa_code = t.fifa_code
      and lower(s.name) = lower(p.name)
  );

drop table _wc_squads;
