-- Stratégie idempotente (re-exécutable sans doublon) :
--   1) UPSERT sur l'index unique (team_id, lower(name)) : insère les nouveaux, actualise
--      numéro/poste/club et réactive (active=true) les joueurs déjà présents.
--   2) DÉSACTIVE (active=false, PAS de DELETE) les joueurs des 48 équipes importées absents de la
--      liste définitive (préliminaires coupés ou variantes de nom remplacées). On préserve ainsi les
--      références FK ON DELETE SET NULL (ref.match_events.player_id, tournament_predictions.top_scorer_player_id).
--   Résultat : pour chaque équipe importée, l'effectif ACTIF = exactement la liste Wikipédia.

